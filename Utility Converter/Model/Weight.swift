import Foundation

class Weight {
    var Kg: Double
    var Grams: Double
    var Ounces: Double
    var Pounds: Double
    var Stonepounds: Double

    init(Kg: Double, Grams: Double, Ounces: Double, Pounds: Double, Stonepounds: Double) {
        self.Kg = Kg
        self.Grams = Grams
        self.Ounces = Ounces
        self.Pounds = Pounds
        self.Stonepounds = Stonepounds
    }
    
}
